function calcBarHeights(currentTemp, maxTemp, minTemp, maxBarHeight, minBarHeight) {
    var height
    var maxHeight = maxBarHeight
    var minHeight = minBarHeight
    var tempRange = maxTemp-minTemp
    if (currentTemp >= maxTemp) {
      height = maxHeight
    } else if (currentTemp <= minTemp) {
      height = minHeight
    } else {
      height = minHeight + ((1.0 * (currentTemp - minTemp) / tempRange) * (maxHeight - minHeight))
    }
    return height
}
